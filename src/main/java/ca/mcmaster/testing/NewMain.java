package ca.mcmaster.testing;

import static java.lang.System.exit;
import static org.apache.log4j.Level.WARN;
import org.apache.log4j.Logger;
import org.apache.log4j.PatternLayout;
import org.apache.log4j.RollingFileAppender;

/**
 *
 * @author tamvadss
 */
public class NewMain {
     
    static {
        Logger logger=Logger.getLogger(NewMain.class);
        logger.setLevel(WARN);
        PatternLayout layout = new PatternLayout("%5p  %d  %F  %L  %m%n");     
        try {
            RollingFileAppender rfa =new  
                RollingFileAppender(layout, NewMain.class.getSimpleName());
            rfa.setMaxBackupIndex(60);
            logger.addAppender(rfa);
            logger.setAdditivity(false);            
             
        } catch (Exception ex) {
            ///
            System.err.println("Exit: unable to initialize logging"+ex);       
            exit(1);
        }
    }

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // TODO code application logic here
        System.out.println("Sai ram");
    }
    
}
